import mongoose from "mongoose";

const serviceProviderSchema = new mongoose.Schema({
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  mobileNumber: {
    type: String,
    required: true,
  },
  fname: {
    type: String,
    required: true,
  },
  lname: {
    type: String,
    required: true,
  },
  city:{
    type:String,
    required:true
  }
  
});

export default mongoose.model("ServiceProvider", serviceProviderSchema);
